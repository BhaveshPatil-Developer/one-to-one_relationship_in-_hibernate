package com.bhavesh;

public class Address {
	int addId;
	String city;
	int pin;
	public int getAddId() {
		return addId;
	}
	public void setAddId(int addId) {
		this.addId = addId;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public int getPin() {
		return pin;
	}
	public void setPin(int pin) {
		this.pin = pin;
	}
	@Override
	public String toString() {
		return "Address [addId=" + addId + ", city=" + city + ", pin=" + pin + "]";
	}
	

}
